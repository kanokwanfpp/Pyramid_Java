public class fon20 {
 public static void main(String args[])
    {
        int fon=5;
        for(int i=1;i<=fon;i++)
	{
            for(int j=1;j<=i;j++)
	    {
                System.out.print(i);
            }
            for(int j=1;j<=i;j++)
	    {
                System.out.print("*");
            }
            for(int j=1;j<=i;j++)
	    {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
    